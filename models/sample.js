const mongoose = require("mongoose")
const sampleSchema = new mongoose.Schema({
    title:{
        type:String,
        required:true
    },
    description:{
        type:String,
        required:true
    },
    location:{
        type:String,
        required:true
    },
    startTime:{
        type:String,
        required:true
    },
    endTime:{
        type:String,
        required:true
    },
})

const SAMPLE = mongoose.model("SAMPLE", sampleSchema)

module.exports = SAMPLE